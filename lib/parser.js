  line = line.replace(/\r\n$/, '\n')

  // ignore empty lines, except if they are (or could be) part of yaml
  // >\nfoo\n\nbar\n is yaml for `"foo\nbar"`
  // >\nfoo\nbar\n is yaml for `"foo bar"`
